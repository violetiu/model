package org.violetime.model.config;

public class ModelConfig implements IModelConfig {
    private String name,command,container,image,text,jar;
    private boolean isEnable,isStructure,isArgs;

    public String getJar() {
        return jar;
    }

    public void setJar(String jar) {
        this.jar = jar;
    }

    public ModelConfig() {
        this.isArgs = false;
        this.isEnable=false;
        this.isStructure=false;
    }

    public boolean isArgs() {
        return isArgs;
    }

    public void setArgs(boolean args) {
        isArgs = args;
    }

    public boolean isStructure() {
        return isStructure;
    }

    public void setStructure(boolean structure) {
        isStructure = structure;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public boolean isEnable() {
        return isEnable;
    }

    public void setEnable(boolean enable) {
        isEnable = enable;
    }

    @Override
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String getCommand() {
        return command;
    }

    public void setCommand(String command) {
        this.command = command;
    }

    @Override
    public String getContainer() {
        return container;
    }

    public void setContainer(String container) {
        this.container = container;
    }
}
