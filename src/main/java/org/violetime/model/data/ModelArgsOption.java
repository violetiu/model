package org.violetime.model.data;

public class ModelArgsOption {
    private String key;
    private String text;

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getText() {
        return text;
    }

    public ModelArgsOption(String key, String text) {
        this.key = key;
        this.text = text;
    }

    public ModelArgsOption() {

    }

    /**
     * 需要提前在多语言中定义好
     * @param text
     */
    public void setText(String text) {
        this.text = text;
    }
}

